#include <iostream>

using namespace std;

int main() {
    // Declare two integer variables and one float variable
    int intValue1, intValue2;
    float floatValue;

    // Assign values to the variables
    intValue1 = 20;
    intValue2 = 18;
    floatValue = 14.2;

    // Print the values on the screen
    cout << "First integer value: " << intValue1 << endl;
    cout << "Second integer value: " << intValue2 << endl;
    cout << "Float value: " << floatValue << endl;

    return 0;
}
